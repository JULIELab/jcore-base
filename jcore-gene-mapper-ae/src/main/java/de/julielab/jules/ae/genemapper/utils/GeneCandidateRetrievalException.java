package de.julielab.jules.ae.genemapper.utils;

public class GeneCandidateRetrievalException extends GeneMapperException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -5875237286044582476L;

	public GeneCandidateRetrievalException() {
		super();
	}

	public GeneCandidateRetrievalException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public GeneCandidateRetrievalException(String message, Throwable cause) {
		super(message, cause);
	}

	public GeneCandidateRetrievalException(String s) {
		super(s);
	}

	public GeneCandidateRetrievalException(Throwable cause) {
		super(cause);
	}

}
