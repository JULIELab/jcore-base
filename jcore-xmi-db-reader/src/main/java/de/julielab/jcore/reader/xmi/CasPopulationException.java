package de.julielab.jcore.reader.xmi;

public class CasPopulationException extends Exception {
    public CasPopulationException() {
    }

    public CasPopulationException(String message) {
        super(message);
    }

    public CasPopulationException(String message, Throwable cause) {
        super(message, cause);
    }

    public CasPopulationException(Throwable cause) {
        super(cause);
    }

    public CasPopulationException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
