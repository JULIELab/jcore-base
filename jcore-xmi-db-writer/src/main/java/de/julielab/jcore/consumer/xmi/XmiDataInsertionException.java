package de.julielab.jcore.consumer.xmi;

public class XmiDataInsertionException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -5479379914342553206L;

	public XmiDataInsertionException() {
		super();
	}

	public XmiDataInsertionException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public XmiDataInsertionException(String message, Throwable cause) {
		super(message, cause);
	}

	public XmiDataInsertionException(String message) {
		super(message);
	}

	public XmiDataInsertionException(Throwable cause) {
		super(cause);
	}

}
