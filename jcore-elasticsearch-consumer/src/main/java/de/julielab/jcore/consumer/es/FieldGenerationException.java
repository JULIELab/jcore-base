package de.julielab.jcore.consumer.es;

public class FieldGenerationException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 6701274292084660697L;

	public FieldGenerationException() {
		super();
	}

	public FieldGenerationException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public FieldGenerationException(String message, Throwable cause) {
		super(message, cause);
	}

	public FieldGenerationException(String message) {
		super(message);
	}

	public FieldGenerationException(Throwable cause) {
		super(cause);
	}


}
